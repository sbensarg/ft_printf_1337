int			len_num(long n)
{
	int count;

	count = 0;
	if (n >= 0 && n <= 9)
		return (1);
	if (n < 0)
	{
		n = n * -1;
        count++;
	}
	while (n)
	{
		n /= 10;
		++count;
	}
	return (count);
}
