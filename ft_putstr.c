#include <unistd.h>

void	ft_putstr(char *s, int *ptr)
{
	int i;
	
	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
		*ptr = *ptr + 1;
	}
}
